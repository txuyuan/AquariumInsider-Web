    <h2>Recognising Complications</h2>

    <p>
      If you take good care of your aquarium, it is rare for your fish to fall
      sick. However, there is no guarantee that you will never have to treat
      your fish for a disease. By maintaining proper water chemistry, pristine
      water quality, feeding your pets a variety of high-quality fish foods, and
      providing them with an optimal habitat and suitable tank mates, you can
      avoid most complications that arise.
    </p>

    <h3>Common Reasons</h3>
    <p>
      Almost all fish-related complications come from the creatures being
      stressed, which weakens the immune system. There are a myriad of factors,
      including but not limited to:
    </p>
    <ul>
      <li>Shipping</li>
      <li>Poor water quality</li>
      <li>Improper water chemistry</li>
      <li>Inadequate filtration</li>
      <li>Improper diet</li>
      <li>Overcrowding</li>
      <li>Lack of day/night cycle (leaving the light on)</li>
      <li>Injuries</li>
      <li>Aggression from other fish</li>
      <li>Inadequate habitat</li>
    </ul>

    Here are some ways to prevent such factors from affecting your aquarium:
    <ul>
      <li>
        Don’t purchase new arrivals at the aquarium. Make sure that they’re at
        least a week old, and remember to acclimate them to your aquariums after
        bringing them home, spending around 30 minutes doing so.
      </li>
      <li>
        Make sure there is enough foliage for all your fish and rearrange
        decorations if necessary, so as to dissuade established territorial fish
        from harassing the new pets.
      </li>
      <li>
        When introducing new fish to your aquarium, keep them in the dark for a
        few hours to allow them to adjust to their new environment
      </li>
    </ul>

    <h3>Solutions</h3>

    <p>
      Quarantining new fish could be a possible solution, by keeping a separate
      ‘Quarantine Aquarium’. This would reduce the chances of introducing a
      disease organism into your aquarium. It also serves as a place to safely
      treat sick fish if or when necessary, without introducing chemicals to
      your main tank. This tank could also be used to isolate bullies or fish
      that are being picked on, as sort of a ‘solitary confinement’.
    </p>

    <p>
      You could also make use of equipment such as Ultraviolet Sterilisers,
      which kill disease-causing organisms, suspended algae and help keep water.
      You should change your steriliser every 10 months or 7,000 hours of
      operation to maintain peak performance.
    </p>

    <h3>Recognising</h3>

    <p>
      But most importantly, is the ability to recognize any issues that arise so
      that they may be addressed. To do so, you should be able to understand
      your fishes’ ‘normal’ behaviour, so as to observe any irregularities. A
      good time for observation is when fishes are feeding.
    </p>

    There are common symptoms of complications, including but not limited to:
    <ul>
      <li>White spots</li>
      <li>Cloudy eyes</li>
      <li>Bloody patches</li>
      <li>White body film</li>
      <li>Torn / ragged fins</li>
      <li>Loss of colour</li>
    </ul>

    Behaviour that indicate possible complications with the aquarium include:
    <ul>
      <li>Gasping at the surface</li>
      <li>Shimmying</li>
      <li>Rubbing against decorations or the substrate</li>
      <li>Cowering</li>
      <li>Refusing food</li>
      <li>Dashing around</li>
    </ul>
